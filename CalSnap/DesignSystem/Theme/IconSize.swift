import Foundation

/// Standard icon sizes for consistent UI
struct IconSize {
    static let xs: CGFloat = 12   // Very small icons (inline text)
    static let sm: CGFloat = 16   // Small icons (macro rows, labels)
    static let md: CGFloat = 24   // Medium icons (buttons, cards)
    static let lg: CGFloat = 32   // Large icons (prominent displays)
    static let xl: CGFloat = 40   // Extra large icons (hero elements)
    static let xxl: CGFloat = 48  // Extra extra large icons (empty states)
}

